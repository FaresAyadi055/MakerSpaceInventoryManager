<template>
  <div class="purchase-list-view">
    <Navbar />
    
    <div class="main-container">
      <div class="content-wrapper">
        <!-- Header -->
        <div class="header-section">
          <div>
            <h1 class="text-3xl font-bold text-surface-900">
              Purchase List Management
            </h1>
            <br />
            <p class="text-surface-600 mt-2">
              View purchase recommendations based on inventory and pending requests
            </p>
          </div>
          <br />
          <div class="flex items-center gap-4">
          </div>
        </div>

        <!-- Search and Tools -->
        <div class="card mb-6">
          <div class="flex justify-between items-left p-4">
            <div class="flex items-center gap-4 w-full">
              <span class="p-input-icon-left w-full md:w-96">
                <i class="pi pi-search" />
                <InputText 
                  v-model="searchQuery" 
                  placeholder="Search across all columns..." 
                  class="w-full"
                  @input="handleSearch"
                />
              </span>
            </div>
            <br>
            <div class="flex items-center gap-2">
              <Button 
                label="Refresh" 
                icon="pi pi-refresh" 
                @click="loadData"
                :loading="loading"
              />
              <Button 
                label="Export CSV"
                icon="pi pi-file-export" 
                class="ml-4"
                @click="exportCSV(filteredPurchaseList, 'purchase_list_export.csv')"
              />
            </div>
          </div>
        </div>
        <br>

        <!-- Main Content Area -->
        <div class="main-content-area">
          <!-- Table Section -->
          <div class="table-section">
            <!-- DataTable -->
            <div class="card">
              <DataTable
                :value="filteredPurchaseList"
                :paginator="true"
                :rows="10"
                :rowsPerPageOptions="[5, 10, 20, 50]"
                :loading="loading"
                paginatorTemplate="FirstPageLink PrevPageLink PageLinks NextPageLink LastPageLink CurrentPageReport RowsPerPageDropdown"
                currentPageReportTemplate="Showing {first} to {last} of {totalRecords} items"
                responsiveLayout="scroll"
                class="p-datatable-sm"
                sortField="needed_to_purchase"
                :sortOrder="-1"
              >
                <!-- ID Column -->
                <Column field="id" header="ID" :sortable="true">
                  <template #body="{ data }">
                    <Badge :value="data.id" severity="info" />
                  </template>
                </Column>

                <!-- Model Column -->
                <Column field="model" header="Model" :sortable="true" />

                <!-- Description Column -->
                <Column field="description" header="Description" :sortable="true" />

                <!-- Stock Column -->
                <Column field="stock" header="Stock" :sortable="true">
                  <template #body="{ data }">
                    <Badge 
                      :value="data.stock" 
                      :severity="getStockSeverity(data.stock)"
                    />
                  </template>
                </Column>

                <!-- Request Count Column -->
                <Column field="request_count" header="Pending Requests" :sortable="true">
                  <template #body="{ data }">
                    <Badge 
                      :value="data.request_count" 
                      severity="contrast"
                    />
                  </template>
                </Column>

                <!-- Total Requested Column -->
                <Column field="total_requested" header="Total Requested" :sortable="true">
                  <template #body="{ data }">
                    <Badge 
                      :value="data.total_requested" 
                      severity="warning"
                    />
                  </template>
                </Column>

                <!-- Location Column -->
                <Column field="location" header="Location" :sortable="true">
                  <template #body="{ data }">
                    <Badge :value="data.location" severity="info" />
                  </template>
                </Column>

                <!-- Needed to Purchase Column -->
                <Column field="needed_to_purchase" header="minimum purchase qty" :sortable="true">
                  <template #body="{ data }">
                    <Badge 
                      :value="data.needed_to_purchase" 
                      :severity="getPurchaseSeverity(data.needed_to_purchase)"
                    />
                  </template>
                </Column>

                <!-- Status Column -->
                <Column field="needed_to_purchase" header="Status" :sortable="true">
                  <template #body="{ data }">
                    <Tag 
                      :value="getStatusText(data.needed_to_purchase, data.stock)" 
                      :severity="getStatusSeverity(data.needed_to_purchase, data.stock)"
                      rounded
                    />
                  </template>
                </Column>
              </DataTable>
            </div>

            <!-- Stats Cards -->
            <br>
            <div class="grid grid-cols-1 md:grid-cols-4 gap-6 mt-8">
              <div class="card text-center p-6">
                <div class="text-3xl font-bold text-primary-600">{{ totalItems }}</div>
                <div class="text-surface-600 mt-2">Total Items</div>
              </div>
              <br>
              <div class="card text-center p-6">
                <div class="text-3xl font-bold text-red-600">{{ urgentPurchaseItems }}</div>
                <div class="text-surface-600 mt-2">Urgent Purchase Needed</div>
              </div>
              <br>
              <div class="card text-center p-6">
                <div class="text-3xl font-bold text-blue-600">{{ pendingRequests }}</div>
                <div class="text-surface-600 mt-2">Total Pending Requests</div>
              </div>
              <br>
              <div class="card text-center p-6">
                <div class="text-3xl font-bold text-green-600">{{ totalNeededToPurchase }}</div>
                <div class="text-surface-600 mt-2">Total Needed to Purchase</div>
              </div>
            </div>
          </div>
        </div>
      </div>
    </div>

    <!-- Toast -->
    <Toast />
  </div>
</template>

<script setup>
import { ref, computed, onMounted } from 'vue'
import { useRouter } from 'vue-router'
import Button from 'primevue/button'
import DataTable from 'primevue/datatable'
import Column from 'primevue/column'
import InputText from 'primevue/inputtext'
import Badge from 'primevue/badge'
import Tag from 'primevue/tag'
import Toast from 'primevue/toast'
import { useToast } from 'primevue/usetoast'
import Navbar from '@/components/Navbar.vue'
import { exportCSV } from '@/utils/exportCSV.js'

const router = useRouter()
const toast = useToast()
const baseURL = import.meta.env.VITE_API_BASE_URL || 'http://localhost:4000/api'

// State
const user = ref(JSON.parse(localStorage.getItem('user') || '{}'))
const purchaseList = ref([])
const loading = ref(false)
const searchQuery = ref('')

onMounted(() => {
  checkAdminAccess()
  loadData()
})

// Check if user is admin
const checkAdminAccess = () => {
  const userRole = user.value?.role
  if (userRole !== 'admin') {
    toast.add({
      severity: 'error',
      summary: 'Access Denied',
      detail: 'Only administrators can access this page',
      life: 3000
    })
    router.push('/')
  }
}

// Computed properties
const totalItems = computed(() => purchaseList.value.length)

const urgentPurchaseItems = computed(() => {
  return purchaseList.value.filter(item => {
    const needed = parseInt(item.needed_to_purchase) || 0
    return needed > 0
  }).length
})

const pendingRequests = computed(() => {
  return purchaseList.value.reduce((sum, item) => {
    const count = parseInt(item.request_count) || 0
    return sum + count
  }, 0)
})

const totalNeededToPurchase = computed(() => {
  return purchaseList.value.reduce((sum, item) => {
    const needed = parseInt(item.needed_to_purchase) || 0
    return sum + needed
  }, 0)
})

// Filter purchase list based on search
const filteredPurchaseList = computed(() => {
  if (!searchQuery.value.trim()) {
    return purchaseList.value
  }
  
  const searchTerm = searchQuery.value.toLowerCase().trim()
  return purchaseList.value.filter(item => {
    return Object.values(item).some(value => {
      if (value === null || value === undefined) return false
      return String(value).toLowerCase().includes(searchTerm)
    })
  })
})

// Load data
const loadData = async () => {
  loading.value = true
  try {
    const response = await fetch(baseURL + '/complex', {
      headers: {
        'Authorization': `Bearer ${localStorage.getItem('token')}`
      }
    })
    
    if (!response.ok) {
      if (response.status === 401) {
        localStorage.removeItem('token')
        localStorage.removeItem('user')
        router.push('/login')
        return
      }
      throw new Error(`HTTP error! status: ${response.status}`)
    }
    
    const data = await response.json()
    
    if (data.success && data.data) {
      purchaseList.value = data.data
      
      toast.add({
        severity: 'success',
        summary: 'Data Loaded',
        detail: `Loaded ${data.data.length} purchase recommendations`,
        life: 3000
      })
    } else {
      throw new Error(data.message || 'Failed to load data')
    }
  } catch (error) {
    console.error('Error loading purchase list:', error)
    toast.add({
      severity: 'error',
      summary: 'Error',
      detail: error.message || 'Failed to load purchase list data',
      life: 5000
    })
  } finally {
    loading.value = false
  }
}

// Handle search
const handleSearch = () => {
  // Optional: Add debounce here if needed
}

// Helper functions for severity colors
const getStockSeverity = (stock) => {
  const stockNum = parseInt(stock) || 0
  if (stockNum === 0) return 'danger'
  if (stockNum < 5) return 'warning'
  if (stockNum < 10) return 'info'
  return 'success'
}

const getPurchaseSeverity = (needed) => {
  const neededNum = parseInt(needed) || 0
  if (neededNum > 0) return 'danger'
  return 'success'
}

const getStatusText = (needed, stock) => {
  const neededNum = parseInt(needed) || 0
  const stockNum = parseInt(stock) || 0
  
  if (neededNum > 0) {
    return 'URGENT - Purchase Needed'
  } else if (stockNum === 0) {
    return 'OUT OF STOCK'
  } else if (stockNum < 5) {
    return 'LOW STOCK'
  } else {
    return 'STOCK OK'
  }
}

const getStatusSeverity = (needed, stock) => {
  const neededNum = parseInt(needed) || 0
  const stockNum = parseInt(stock) || 0
  
  if (neededNum > 0) {
    return 'danger'
  } else if (stockNum === 0) {
    return 'danger'
  } else if (stockNum < 5) {
    return 'warning'
  } else {
    return 'success'
  }
}
</script>

